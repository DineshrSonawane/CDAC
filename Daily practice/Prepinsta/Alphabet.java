package prepinsta;

public class Alphabet {

	public static void main(String[] args) {
		char ch;
        
        ch = '0';
		
	 if((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z'))
	      System.out.println("The character " + ch + " is an Alphabet");
	else
	      System.out.println("The character " + ch + " is not an Alphabet");
		
				         
	}

}
